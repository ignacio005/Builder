public class Sandwich {
    private String pan;
    private String relleno;

    public void setPan(String pan) {
        this.pan = pan;
    }

    public void setRelleno(String relleno) {
        this.relleno = relleno;
    }

    public String getPan() {
        return pan;
    }

    public String getRelleno(){
        return relleno;
    }
}
